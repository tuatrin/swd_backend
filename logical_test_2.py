ROMAN = [
    (1000, "M"),
    ( 900, "CM"),
    ( 500, "D"),
    ( 400, "CD"),
    ( 100, "C"),
    (  90, "XC"),
    (  50, "L"),
    (  40, "XL"),
    (  10, "X"),
    (   9, "IX"),
    (   5, "V"),
    (   4, "IV"),
    (   1, "I"),
]

def roman_converter():
    input_number = int(input('Input 1 - 1000: '))
    result = ""
    if input_number >= 1 and input_number <= 1000:

      for (arabic, roman) in ROMAN:

          factor = input_number//arabic

          if factor > 0:
            input_number -= arabic * factor
            result += roman * factor

      print(result)
    else:
      print('Input number is out of range')

if __name__ == "__main__":
    roman_converter()